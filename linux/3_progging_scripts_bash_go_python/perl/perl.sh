#airgap perl module install
# https://www.ibm.com/docs/en/imdm/11.5?topic=modules-installing-perl-without-internet-connection
# https://cpan.metacpan.org/authors/id/Z/ZA/ZARQUON/DBD-Oracle-1.83.tar.gz

For each of the modules that you downloaded, complete the following steps:
Unpack it into a writeable directory.
Run the Perl configure command: perl Makefile.pl.
Run the make command.
Run the make test command.
Do not proceed until this command completes successfully.
Run the make install command.